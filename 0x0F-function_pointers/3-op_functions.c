#include "3-calc.h"

/**
 *op_add - sums 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 *op_sub - subtracts 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 *op_mul - returns product
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 *op_div - returns result of division
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 *op_mod - returns remainder of division
 * @a: first number
 * @b: second number
 *
 * Return: results
 */
int op_mod(int a, int b)
{
	return (a % b);
}
